#include "calc.h"
#include <stdio.h>

/**
 * op_add - add function
 *
 * @a: input 1
 * @b: input 2
 *
 */



int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - subtract
 *
 * @a: input 1
 * @b: input 2
 *
 */


int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - multiply
 *
 * @a: input 1
 * @b: input 2
 *
 */


int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - divide
 *
 * @a: input 1
 * @b: input 2
 *
 */


int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - modulus
 *
 * @a: input 1
 * @b: input 2
 *
 */


int op_mod(int a, int b)
{
	return (a % b);
}
